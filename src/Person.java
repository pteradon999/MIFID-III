public class Person {
    public String Name;
    public String Att_Type;
    public String Pers_Code;
    public int cap_value;
    public int net_value;
    public int ver_value;
    public String education;
    public String country;
    public String workplace;
    public int exp;
    public String language;


    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getAtt_Type() {
        return Att_Type;
    }

    public void setAtt_Type(String att_Type) {
        Att_Type = att_Type;
    }

    public String getPers_Code() {
        return Pers_Code;
    }

    public void setPers_Code(String pers_Code) {
        Pers_Code = pers_Code;
    }

    public int getCap_value() {
        return cap_value;
    }

    public void setCap_value(int cap_value) {
        this.cap_value = cap_value;
    }

    public int getNet_value() {
        return net_value;
    }

    public void setNet_value(int net_value) {
        this.net_value = net_value;
    }

    public int getVer_value() {
        return ver_value;
    }

    public void setVer_value(int ver_value) {
        this.ver_value = ver_value;
    }

    public String getEducation() {
        return education;
    }

    public void setEducation(String education) {
        this.education = education;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getWorkplace() {
        return workplace;
    }

    public void setWorkplace(String workplace) {
        this.workplace = workplace;
    }

    public int getExp() {
        return exp;
    }

    public void setExp(int exp) {
        this.exp = exp;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }
}
